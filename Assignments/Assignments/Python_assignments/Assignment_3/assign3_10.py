
# Python manages memory automatically using a process called garbage collection.
## Garbage collection is a form of automatic memory management that frees up memory occupied by objects that are no longer in use by the program.
# hence the programmer does not need to explicitly free memory as in C/C++ or Java. Python uses reference counting and a cyclic garbage collector to manage memory. 
# When an object's reference count drops to zero, it is automatically deallocated.
